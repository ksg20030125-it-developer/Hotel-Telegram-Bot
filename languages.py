# Default language - Serbian
DEFAULT_LANGUAGE = 'sr'  # Serbian as default


def get_text(key: str, lang: str = None, **kwargs) -> str:
    """
    Get translated text for a key
    
    Args:
        key: Translation key
        lang: Language code (en, sr). If None, uses default (Serbian)
        **kwargs: Format parameters for string formatting
    
    Returns:
        Translated text
    """
    if lang is None:
        lang = DEFAULT_LANGUAGE
    
    # Fallback to Serbian if language not found
    if lang not in TRANSLATIONS:
        lang = 'sr'
    
    # Get translation
    text = TRANSLATIONS.get(lang, {}).get(key, key)
    
    # Format with parameters if provided
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    
    return text


def set_user_language(user_id: int, lang: str, db=None):
    """
    Set language preference for a user in database
    
    Args:
        user_id: Telegram user ID
        lang: Language code
        db: DatabaseManager instance (optional)
    """
    if lang in TRANSLATIONS:
        if db:
            db.update_employee_language(user_id, lang)


def get_user_language(user_id: int, db=None) -> str:
    """
    Get language preference for a user from database
    
    Args:
        user_id: Telegram user ID
        db: DatabaseManager instance (optional)
        
    Returns:
        Language code (default: Serbian 'sr')
    """
    if db:
        return db.get_employee_language(user_id)
    return DEFAULT_LANGUAGE


